     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        
        $immagine = new Immagine;
        $immagine->nomefile = $request->nomefile;
        $immagine->annuncio_id = $request->id;
        $immagine->save();

        return redirect()->route('index');
    }

